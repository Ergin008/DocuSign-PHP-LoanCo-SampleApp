                <strong>Key elements required far a legal e-signature</strong>
                <br/>
                DocuSign provides a comprehensive method for ensuring that all legal elements of proof are managed throughout the electronic document signing cycle, including:
              <ul >
                <li><strong>Audit Trail</strong> – DocuSign ensures the transaction is secure, and all signer actions are tracked in a way that is admissible in court, if required. An audit trail is attached to every DocuSign electronic envelope.</li>
                <li><strong>Security</strong> – DocuSign encrypts all files to ensure only people designated by the sender can read and sign documents. DocuSign uses RSA AES encryption and SHA1 hashing algorithms.</li>
                <li><strong>Unique Signatures</strong> – DocuSign users create unique electronic signatures that combine an ID number and a digital image stamp of their signature and initials. The user controls this e-signature, which is stored securely online.</li>
                <li><strong>Signature Blocks </strong>– DocuSign allows signers to 'initial' and 'sign' specific areas in documents, as well as the whole file, which is critical for many documents.</li>
              </ul>
              <ul >
                <li><strong>Authenticate Users </strong>– DocuSign leverages email and 3rd-party systems to provide auditable authentication of every signer.</li>
                <li><strong>Time-Stamping</strong> – DocuSign timestamps and records all signing events, including document submission, forwarding, viewing and signing.</li>
                <li><strong>Transaction Summary</strong> – DocuSign attaches a document history at the end of every transaction, showing exactly who signed what.  Other recorded data include signing party ID, timestamps for each step, and authentication information.</li>
